export function lookupHandicap(manufacturer, model) {
  const entry = SSA_HANDICAPS.find(
    h => h.manufacturer.toLowerCase() === manufacturer.toLowerCase() &&
         h.model.toLowerCase() === model.toLowerCase()
  );
  return entry || null;
}

export function searchGliders(query) {
  if (!query || query.length < 2) return [];
  const q = query.toLowerCase();
  return SSA_HANDICAPS.filter(h =>
    h.manufacturer.toLowerCase().includes(q) ||
    h.model.toLowerCase().includes(q)
  ).slice(0, 30);
}
